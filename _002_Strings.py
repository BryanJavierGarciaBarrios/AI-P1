variable = "No se encontraron problemas" #Una variable necesita comillas para ser declarado como string
print(variable)
#Para el uso de comillas dentro de un string es necesario usar dobles comillas al exterior
variable = "No se 'encontraron' problemas" 
print(variable)
#O viceversa, comillas simples al exterior y dobles comillas
variable = 'No se "encontraron problemas"'
print(variable)
